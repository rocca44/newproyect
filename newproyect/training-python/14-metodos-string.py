animal = "VACA lola "
print (animal.strip().capitalize()) # Capitali
print (animal.title()) # pone la primera letra del texto en mayuscula
print (animal.strip()) # elimina los espacios izquierda y derecha
print (animal.rstrip()) #elimina los espacios a la derecha
print (animal.lstrip()) # elimina los espacios a la izquierda
print (animal.find("lo")) # busca y devuelve en que posicion esta lo que quieres
print ("lola" in animal) # busca si existe la palabra devuelve true o false
print ("lola" not in animal)  # pregunta si no esta , es decir lo niega